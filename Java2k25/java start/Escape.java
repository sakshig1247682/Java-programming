public class Escape{
    public static void main(String[] args) {
        System.out.println("Hello 'sakshi'");
        System.out.println("Hello \"sakshi\"");
        System.out.println("Hello \nSakshi");
        System.out.println("Hello \tSakshi");
        System.out.println("Hello \\Sakshi");
        System.out.println("Hello\b Sakshi");



    }
}